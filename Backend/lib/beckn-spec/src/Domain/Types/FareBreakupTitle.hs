  Spec.CUSTOMER_SELECTED_FARE -> CUSTOMER_SELECTED_FARE
  Spec.TOTAL_FARE -> TOTAL_FARE
  Spec.WAITING_OR_PICKUP_CHARGES -> WAITING_OR_PICKUP_CHARGES
  Spec.PARKING_CHARGE -> PARKING_CHARGE
  Spec.EXTRA_TIME_FARE -> EXTRA_TIME_FARE
  Spec.NIGHT_SHIFT_CHARGE -> NIGHT_SHIFT_CHARGE
  Spec.FIXED_GOVERNMENT_RATE -> FIXED_GOVERNMENT_RATE
  Spec.SGST -> SGST
  Spec.CGST -> CGST
  Spec.PLATFORM_FEE -> PLATFORM_FEE
  Spec.TIME_BASED_FARE -> TIME_BASED_FARE
  Spec.DIST_BASED_FARE -> DIST_BASED_FARE
  Spec.EXTRA_DISTANCE_FARE -> EXTRA_DISTANCE_FARE
  Spec.RIDE_DURATION_FARE -> RIDE_DURATION_FARE
  Spec.INSURANCE_CHARGES -> INSURANCE_CHARGES
  Spec.CARD_CHARGES_ON_FARE -> CARD_CHARGES_ON_FARE
  Spec.CARD_CHARGES_FIXED -> CARD_CHARGES_FIXED


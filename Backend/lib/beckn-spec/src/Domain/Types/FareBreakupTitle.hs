module Domain.Types.FareBreakupTitle
  ( FareBreakupTitle (..),
    toDomainFBTitle,
    toBecknQBTitle,
  )
where

import qualified BecknV2.OnDemand.Enums as Spec
import Data.Aeson
import EulerHS.Prelude (Hashable)
import Kernel.Prelude

data FareBreakupTitle
  = BASE_FARE
  | DISTANCE_FARE
  | CANCELLATION_CHARGES
  | TOLL_CHARGES
  | CONGESTION_CHARGE
  | SERVICE_CHARGE
  | DEAD_KILOMETER_FARE
  | DRIVER_SELECTED_FARE
  | CUSTOMER_SELECTED_FARE
  | TOTAL_FARE
  | WAITING_OR_PICKUP_CHARGES
  | PARKING_CHARGE
  | EXTRA_TIME_FARE
  | NIGHT_SHIFT_CHARGE
  | FIXED_GOVERNMENT_RATE
  | SGST
  | CGST
  | PLATFORM_FEE
  | TIME_BASED_FARE
  | DIST_BASED_FARE
  | EXTRA_DISTANCE_FARE
  | RIDE_DURATION_FARE
  | INSURANCE_CHARGES
  | CARD_CHARGES_ON_FARE
  | CARD_CHARGES_FIXED
  deriving (Show, Read, Eq, Generic, ToJSON, FromJSON, Hashable)

toDomainFBTitle :: Spec.QuoteBreakupTitle -> FareBreakupTitle
toDomainFBTitle = \case
  Spec.BASE_FARE -> BASE_FARE
  Spec.DISTANCE_FARE -> DISTANCE_FARE
  Spec.CANCELLATION_CHARGES -> CANCELLATION_CHARGES
